@dataclass(frozen=True, slots=True)
class PromptMessage:
    """旧辅助函数返回的最小消息对象。"""

    content: str


@dataclass(frozen=True, slots=True)
class CompatPromptTemplate:
    """提供历史 ``format_messages`` 接口，不再依赖 LangChain。"""

    template: str

    def format_messages(self, **values: Any) -> list[PromptMessage]:
        return [PromptMessage(self.template.format(**values))]


def formTemplate(prompt: str) -> CompatPromptTemplate:
    return CompatPromptTemplate(prompt)


def formMessage(
    template: CompatPromptTemplate,
    style: str,
    question: str,
    related_text: list[str],
    message_from_router: str,
) -> list[PromptMessage]:  # 这个应该是传给子链的信息
    if len(related_text) < 3:
        raise ValueError("related_text 至少需要三个元素")
    messages = template.format_messages(
        style=style,
        related_text1=related_text[0],
        related_text2=related_text[1],
        related_text3=related_text[2],
        question=question,
        message_from_router=message_from_router,
    )
    return messages


